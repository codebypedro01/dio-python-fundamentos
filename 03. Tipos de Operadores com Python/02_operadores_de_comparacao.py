saldo = 200
saque = 200


# Operadores de comparação retornam um valor booleano (True ou False):
print(saldo == saque)  # Verifica se os valores são iguais
print(saldo != saque)  # Verifica se os valores são diferentes
print(saldo > saque)   # Verifica se saldo é maior que saque
print(saldo < saque)   # Verifica se saldo é menor que saque
print(saldo >= saque)  # Verifica se saldo é maior ou igual a saque
print(saldo <= saque)  # Verifica se saldo é menor ou igual a saque
