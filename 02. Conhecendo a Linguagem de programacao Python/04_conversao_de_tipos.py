print(int(1.988765464)) # Converte o número decimal 1.988765464 para um número inteiro, resultando em 1.
print(int("10")) # Converte a string "10" para um número inteiro, resultando em 10.
print(float("10.10")) # Converte a string "10.10" para um número decimal, resultando em 10.10.
print(float(10)) # Converte o número inteiro 10 para um número decimal, resultando em 10.0.


valor = 10
valor_str = str(valor) # Converte o número inteiro 10 para uma string, resultando em "10".
print(type(valor)) # Exibe o tipo de dado da variável "valor", que é <class 'int'>, indicando que é um número inteiro.
print(type(valor_str)) # Exibe o tipo de dado da variável "valor_str", que é <class 'str'>, indicando que é uma string.


