produto_1 = 20
produto_2 = 10

print(produto_1 + produto_2)    # Soma
print(produto_1 - produto_2)    # Subtração
print(produto_1 * produto_2)    # Multiplicação
print(produto_1 ** produto_2)   # Exponenciação
print(produto_1 / produto_2)    # Divisão
print(produto_1 // produto_2)   # Divisão inteira (retorna o quociente inteiro da divisão)
print(produto_1 % produto_2)    # Módulo (retorna o resto da divisão)

